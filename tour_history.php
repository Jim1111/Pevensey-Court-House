<p>The Robing Room displays next door include the Pevensey Corporation set of weight and measures.


<p>We know Pevensey has had a Court House at least since Tudor times, but the origins are not documented.</p>

<p>The timber ceiling of the Court Room is 17th Century. Nextdoor, the Robing Room dispays the corporation's official weight, measures and balance scales. On dispay is a wedding dress, a beautiful corn dolly and much else.</p>

<p>The Robing Room, Prison Cells and Exercise Yard below date from the Georgian period (1714 to 1830).</p>

<p>The Beadle (in glass case) wears a genuine costume nearly 200 years old.</p>

<p>In the 20th Century, during the Second World War, a captured German Airman was put in the cells.</p>

<p>Bail certificates costing 20p upon release from the dock are available. Also, very popular is our children's new discovery trail which includes a dinosaur's footprint!</p>

