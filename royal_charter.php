<h1>Pevensey's Royal Charter Granted by King John in 1207</h1>

<p>In 1207 the Barons of Pevensey obtained a Charter from King John, which confirmed Pevensey's status as a Corporate Member of Hastings. The Charter states that the land which lies between the port of Pevensey and Langney is "within the liberty of the Five
<img alt="knight" height="330" src="images/knight2.jpg" width="291" style="float: left">Ports", so it must be assumed that an agreement had been made with the Lord of the Manor of Pevensey, as the land was originally within his demesne.</p>

<p>A facsimile of this charter can be seen in the Court House. After obtaining their charter the Barons had a seal struck for their Borough and this is one of our most important exhibits, displayed in the central show case. The seal dates from some year between 1207 and 1230, and is the oldest original Cinque Ports seal, with a ship represented on both sides. King John issued a series of charters to the Cinque Ports in 1206, probably intending to ensure their loyalty in the coming war against France, by which he hoped to regain Normandy, lost to the French King in 1204.</p>

